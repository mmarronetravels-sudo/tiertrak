#!/usr/bin/env node
/**
 * scripts/teardown-district-sandbox.js
 *
 * Local-only SQL emitter for tearing down the synthetic sandbox
 * district created by scripts/seed-district-sandbox.js. Mirrors the
 * seed script's security model: no DB connection, single transaction,
 * hard-fail preflight.
 *
 * Lookup key: data/sandbox-district-roster-template.js → DISTRICT.name.
 * If the operator edited the roster's district name between seed and
 * teardown, this script will not find any rows and abort with a clear
 * RAISE EXCEPTION. (Edit the roster back, or hand-key a DELETE.)
 *
 * FK / CASCADE map (verified against migrations 028, 031, 036):
 *   - tenants.district_id REFERENCES districts(id) ON DELETE RESTRICT
 *       → delete tenants before district
 *   - users.district_id   REFERENCES districts(id) ON DELETE RESTRICT
 *       → delete users before district
 *   - discipline_referrals.tenant_id REFERENCES tenants(id)  (NO ACTION)
 *       → delete referrals before tenants
 *   - discipline_referral_consequences (ON DELETE CASCADE from referral)
 *       → handled by CASCADE when referrals go
 *   - students.tenant_id  REFERENCES tenants(id) ON DELETE CASCADE
 *       → handled by CASCADE when tenants go, but we delete first for
 *         clarity (and because the seed creates per-student rows that
 *         the operator may want to confirm gone before tenants drop)
 *   - discipline_<vocab>.tenant_id REFERENCES tenants(id) ON DELETE CASCADE
 *       → handled by CASCADE when tenants go
 *   - user_school_access (user_id, district_id) REFERENCES users(id, district_id)
 *       ON DELETE CASCADE → handled when users go
 *   - user_school_access (school_tenant_id, district_id) REFERENCES tenants(id, district_id)
 *       ON DELETE CASCADE → handled when tenants go
 *
 * AUDIT ROWS — INTENTIONALLY LEFT BEHIND:
 *   user_school_access_audit has NO foreign keys by design (M031:69-86,
 *   COMMENT ON TABLE). Audit rows must outlive their referent per
 *   FERPA §99.32 record-of-disclosure retention. The teardown will
 *   produce `cascade_user_delete` rows in that audit table when users
 *   are deleted (M031 trigger). DO NOT add a DELETE on
 *   user_school_access_audit to this script.
 *
 * Usage:
 *   node scripts/teardown-district-sandbox.js > /tmp/sandbox-district-teardown.sql
 *   # paste /tmp/sandbox-district-teardown.sql into the target DB's psql session
 */

const path = require('path');

const roster = require(path.join('..', 'data', 'sandbox-district-roster-template'));

function sqlString(s) {
  if (s === null || s === undefined) return 'NULL';
  return `'${String(s).replace(/'/g, "''")}'`;
}

function buildSql() {
  if (!roster.DISTRICT || typeof roster.DISTRICT.name !== 'string' || !roster.DISTRICT.name.trim()) {
    throw new Error('roster.DISTRICT.name must be a non-empty string');
  }

  const lines = [];
  const TAG = 'sandbox_district_teardown';

  lines.push('-- ============================================================================');
  lines.push(`-- Sandbox district fixture — TEARDOWN for "${roster.DISTRICT.name}"`);
  lines.push('-- Generated by scripts/teardown-district-sandbox.js');
  lines.push('-- Single transaction; hard-fails if the district row is not present.');
  lines.push('-- Leaves user_school_access_audit rows behind by design (FERPA §99.32).');
  lines.push('-- ============================================================================');
  lines.push('');
  lines.push('BEGIN;');
  lines.push('');
  lines.push(`DO $${TAG}$`);
  lines.push('DECLARE');
  lines.push('  v_district_id  INTEGER;');
  lines.push('  v_tenant_count INTEGER;');
  lines.push('  v_user_count   INTEGER;');
  lines.push('BEGIN');
  lines.push('  -- Preflight: district row must exist; capture its id.');
  lines.push(`  SELECT id INTO v_district_id FROM districts WHERE name = ${sqlString(roster.DISTRICT.name)};`);
  lines.push('  IF v_district_id IS NULL THEN');
  lines.push(`    RAISE EXCEPTION 'ABORT: no district named % to tear down', ${sqlString(roster.DISTRICT.name)};`);
  lines.push('  END IF;');
  lines.push('');
  lines.push('  -- Safety: count what we are about to delete and report it in NOTICE');
  lines.push('  -- so the operator can sanity-check before COMMIT.');
  lines.push('  SELECT COUNT(*) INTO v_tenant_count FROM tenants WHERE district_id = v_district_id;');
  lines.push('  SELECT COUNT(*) INTO v_user_count   FROM users   WHERE district_id = v_district_id;');
  lines.push('  RAISE NOTICE \'Tearing down district id=%, % tenant(s), % user(s)\',');
  lines.push('               v_district_id, v_tenant_count, v_user_count;');
  lines.push('');
  lines.push('  -- Step 1: discipline_referral_consequences cascades from referrals via');
  lines.push('  -- ON DELETE CASCADE on the composite FK (referral_id, tenant_id) →');
  lines.push('  -- discipline_referrals(id, tenant_id). No explicit DELETE needed; left as');
  lines.push('  -- a comment so the dependency is visible in the teardown.');
  lines.push('');
  lines.push('  -- Step 2: discipline_referrals (NO ACTION on tenant_id FK → must explicit-delete).');
  lines.push('  DELETE FROM discipline_referrals');
  lines.push('   WHERE tenant_id IN (SELECT id FROM tenants WHERE district_id = v_district_id);');
  lines.push('');
  lines.push('  -- Step 3: students (CASCADE will fire on tenant DROP, but explicit-delete');
  lines.push('  -- here so the row count is auditable in the NOTICE chain).');
  lines.push('  DELETE FROM students');
  lines.push('   WHERE tenant_id IN (SELECT id FROM tenants WHERE district_id = v_district_id);');
  lines.push('');
  lines.push('  -- Step 4: users. user_school_access CASCADEs from users via the composite FK');
  lines.push('  -- (user_id, district_id) → users(id, district_id), and the M031 trigger writes');
  lines.push('  -- cascade_user_delete audit rows. Those audit rows are RETAINED per FERPA §99.32.');
  lines.push('  DELETE FROM users WHERE district_id = v_district_id;');
  lines.push('');
  lines.push('  -- Step 5: tenants. Cascades discipline_<vocab> tables and the school-side of');
  lines.push('  -- user_school_access (now already empty after step 4).');
  lines.push('  DELETE FROM tenants WHERE district_id = v_district_id;');
  lines.push('');
  lines.push('  -- Step 6: district row itself.');
  lines.push('  DELETE FROM districts WHERE id = v_district_id;');
  lines.push('');
  lines.push('  RAISE NOTICE \'Sandbox district fixture removed (audit rows retained).\';');
  lines.push('END');
  lines.push(`$${TAG}$;`);
  lines.push('');
  lines.push('COMMIT;');
  lines.push('');

  return lines.join('\n');
}

function main() {
  process.stdout.write(buildSql());
}

try { main(); }
catch (err) {
  process.stderr.write(`teardown-district-sandbox: ${err.message}\n`);
  process.exit(1);
}
